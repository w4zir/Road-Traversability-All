#ifndef RTI_HPP_
#define RTI_HPP_


#include <RTI.h>


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
template <typename PointT>
pcl::RTI<PointT>::RTI () :
neighbour_number_(30)
{
  }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
template <typename PointT>
pcl::RTI<PointT>::~RTI ()
{
  }

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
template <typename PointT> unsigned int
pcl::RTI<PointT>::getNumberOfNeighbours () const
{
  return (neighbour_number_);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
template <typename PointT> void
pcl::RTI<PointT>::setNumberOfNeighbours (unsigned int neighbour_number)
{
  neighbour_number_ = neighbour_number;
}

#define PCL_INSTANTIATE_RTI(T) template class pcl::RTI<T>;

#endif /* RTI_HPP_ */
